def build_prompt(query, context_docs):
    """
    构造 RAG Prompt

    关键：
    - 强制模型只基于上下文回答
    - 限制胡编乱造
    """

    context = "\n\n".join(context_docs)

    prompt = f"""
你是一个企业知识库问答助手，请严格根据提供的上下文回答问题。

【要求】
1. 只能使用上下文内容回答
2. 如果不知道，请回答“根据现有资料无法回答”
3. 不允许编造

【上下文】
{context}

【问题】
{query}

【回答】
"""

    return prompt